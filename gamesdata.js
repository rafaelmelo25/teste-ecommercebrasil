        const gameCardsContainer = document.getElementById("gameCards");
        const searchInput = document.getElementById("searchInput");
        const sortSelect = document.getElementById("sortSelect");
        const loadMoreButton = document.getElementById("loadMoreButton");

let cardsToShow = 6;
const cardsToAdd = 3;

function createGameCard(game) {
    const cardDiv = document.createElement("div");
    cardDiv.className = "col-md-4 py-3";

    const cardHTML = `
        <div class="row">
            <div class="col-12">
                <div class="card">
                    <img class="card-img-top" src="${game.thumb}" alt="Card image cap">
                    <div class="card-body">
                        <h5 class="card-title">${game.title}</h5>
                        <ul class="d-flex justify-content-between">
                            <li>
                                <button class="btn details-button">Detalhe</button>
                            </li>
                            <li>
                                <p class="game-price top" data-original-price="${game.salePrice}"></p>
                                <p class="game-price bottom" data-original-price="${game.normalPrice}"></p>
                            </li>
                            <li>
                                <button class="btn discount-button">${(game.savings * 1).toFixed(2)}%</button>
                            </li>
                        </ul>
                    </div>
                </div>
            </div>
        </div>
    `;

    cardDiv.innerHTML = cardHTML;
    gameCardsContainer.appendChild(cardDiv);
}

function showGameCards(start, end) {
    // Limpa os cartões existentes
    gameCardsContainer.innerHTML = "";

    // Cria os novos cartões para exibição
    for (let i = start; i < end; i++) {
        if (i >= gamesData.length) {
            loadMoreButton.style.display = "none";
            break;
        }
        createGameCard(gamesData[i]);
    }
}

function filterGames(searchTerm) {
    const filteredGames = gamesData.filter((game) =>
        game.title.toLowerCase().includes(searchTerm.toLowerCase())
    );
    return filteredGames;
}

function loadGames() {
    const searchTerm = searchInput.value;
    const filteredGames = filterGames(searchTerm);

    cardsToShow = cardsToAdd; // Redefine a quantidade de cartões a serem mostrados

    // Exibe os cartões filtrados
    showGameCards(0, cardsToShow);

    // Exibe o botão "Carregar Mais" se houver mais jogos para mostrar
    if (filteredGames.length > cardsToShow) {
        loadMoreButton.style.display = "block";
    } else {
        loadMoreButton.style.display = "none";
    }
}

// Event listener para o campo de pesquisa
searchInput.addEventListener("input", loadGames);

function sortGamesBy(option) {
    gamesData.sort((a, b) => {
        if (option === 'discount') {
            return parseFloat(b.savings) - parseFloat(a.savings);
        } else if (option === 'highest-price') {
            return parseFloat(b.salePrice) - parseFloat(a.salePrice);
        } else if (option === 'lowest-price') {
            return parseFloat(a.salePrice) - parseFloat(b.salePrice);
        } else if (option === 'title') {
            return a.title.localeCompare(b.title);
        }
    });

    gameCardsContainer.innerHTML = ''; // Limpa os cartões existentes
    showGameCards(0, cardsToShow); // Mostra os cartões classificados
}

// Ouvinte de evento para o seletor "Ordenar por"
const orderBySelect = document.getElementById('sortSelect');
orderBySelect.addEventListener('change', () => {
    const selectedOption = orderBySelect.value;
    sortGamesBy(selectedOption);
});

// Função para carregar e exibir os jogos iniciais
function loadGames() {
    showGameCards(0, cardsToShow);
}

loadGames();

function loadMore() {
    const currentCards = document.querySelectorAll(".col-md-4");
    const start = currentCards.length;
    const end = start + cardsToAdd;

    if (start >= gamesData.length) {
        loadMoreButton.style.display = "none"; // Oculta o botão quando todos os jogos foram carregados
        return;
    }

    showGameCards(start, end);
}

loadMoreButton.addEventListener("click", loadMore);